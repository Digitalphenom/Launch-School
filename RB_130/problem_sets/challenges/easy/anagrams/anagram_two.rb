class Anagram
  attr_reader :word

  def initialize(word)
    @word = word.downcase
  end

  def match(array)
    sorted_word = word.chars.sort.join

    array.select do |anagram|
      anagram = anagram.downcase
      next if word == anagram

      sorted_anagram = anagram.chars.sort.join
      sorted_anagram == sorted_word
    end
  end
end